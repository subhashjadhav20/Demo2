
public class SwappingTwoNumber {

	public static void main(String[]args) {
		int a=10;
		int b=20;
		int temp;
		System.out.println("Before swapping");
		System.out.println("a:"+a+"& b:"+b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("after swapping");
		System.out.println("a:"+a+"& b:"+b);
	}
}
